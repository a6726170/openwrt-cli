def lookup(mac: str) -> Optional[str]:
    """
    根据 MAC 地址查询厂商名。
    
    Args:
        mac: MAC 地址，格式支持：
             - 冒号分隔：00:1A:2B:3C:4D:5E
             - 横线分隔：00-1A-2B-3C-4D-5E
             - 无分隔符：001A2B3C4D5E
    
    Returns:
        厂商名，未知返回 None
    """
    if not mac:
        return None
    
    # 统一转小写 + 提取前6位（OUI）
    normalized = mac.strip().upper()
    normalized = normalized.replace("-", ":")
    # 移除多余冒号，保留标准格式
    parts = normalized.split(":")
    if len(parts) == 1:
        # 无分隔符格式
        normalized = ":".join([parts[0][i:i+2] for i in range(0, 6, 2)])
    elif len(parts) == 6:
        normalized = ":".join(p[:2] for p in parts[:6])
    else:
        normalized = ":".join(p[:2] for p in parts[:6])
    
    # 查询 OUI（只取前6位）
    prefix = normalized[:8]  # 00:1A:2B
    if prefix in _MAC_VENDORS:
        return _MAC_VENDORS[prefix]
    
    # 再尝试 00:XX:XX
    prefix3 = normalized[:8]
    return _MAC_VENDORS.get(prefix3)


def lookup_enhanced(mac: str) -> dict:
    """
    返回更详细的设备推断。
    
    Returns:
        dict: {
            "vendor": str,        # 厂商名
            "type": str,           # 设备类型推断（phone/pc/laptop/camera/router/unknown）
            "confidence": str,    # 可信度（high/medium/low）
            "reason": str,         # 判断依据
        }
    """
    vendor = lookup(mac)
    hostname = None  # 可由调用方补充
    
    if vendor is None:
        return {"vendor": None, "type": "unknown", "confidence": "low", "reason": "MAC OUI 未匹配"}
    
    # 厂商特征映射到设备类型
    phone_vendors = {"Apple", "Samsung", "Xiaomi", "Huawei", "Xiaomi", "OPPO", "vivo", "OnePlus"}
    pc_vendors = {"Intel", "Realtek", "Qualcomm", "AMD", "NVIDIA"}
    camera_vendors = {"Hikvision/iQiYi", "Dahua", "Axis", "Bosch"}
    router_vendors = {"TP-Link", "NetGear", "ASUS", "Netgear"}
    cloud_vendors = {"Cloud/Unknown"}
    
    if vendor in phone_vendors:
        vtype = "phone"
        conf = "high"
    elif vendor in pc_vendors:
        vtype = "pc"
        conf = "medium"
    elif vendor in camera_vendors:
        vtype = "camera"
        conf = "high"
    elif vendor in router_vendors:
        vtype = "router"
        conf = "high"
    elif vendor in {"QEMU/KVM", "VirtualBox", "VMware", "Hyper-V", "Parallels", "Xen", "MS Virtual"}:
        vtype = "virtual"
        conf = "high"
    elif vendor == "Cloud/Unknown":
        vtype = "cloud/hosted"
        conf = "medium"
    else:
        vtype = "unknown"
        conf = "low"
    
    return {"vendor": vendor, "type": vtype, "confidence": conf, "reason": f"OUI匹配: {vendor}"}


